# Programa de teste de operações matemáticas

x = 25
y = 5
soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y
potencia = x ** y

print ( x, " + ", y, " = ", soma )
print ( x, " - ", y, " = ", subtracao )
print ( x, " * ", y, " = ", multiplicacao )
print ( x, " / ", y, " = ", divisao )
print ( x, " ** ", y, " = ", potencia )

