# A execução mais básica de uso da função print() é a chama sem argumentos.
# Essa chamada  produz um caracter de nova linha ("\n")
print("Antes do print vazio")
print()
print("Depois do print vazio")

###############################################################################
# A função print() imprime todos seus argumentos em sequência, com um espaço entre cada argumento
print("maça", "banana", "batata", "laranja")

