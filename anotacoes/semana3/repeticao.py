# A instrução for permite a repetição das instruções aningadas. No caso abaixo,
# pra cada valor dentro da lista, atribui o valor à vairável amigo e executa a 
# instrução aninhada.
for amigo in ["Joe", "Amy", "Brad", "Angelina", "Zuki", "Thandi", "Paris"]:
    print("Ola ", amigo, "  Por favor venha a minha festa no sabado!")